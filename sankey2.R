# Library
library(networkD3)
library(dplyr)
 
# A connection data frame is a list of flows with intensity for each flow
links <- data.frame(
  source=c("8 puestos en Ene19", "10 puestos en Ene19", "11 puestos en Ene19", "11 puestos en Feb19", "11 puestos en Feb19", "11 puestos en Mar19", "10 puestos en Mar19"), 
  target=c("11 puestos en Feb19", "11 puestos en Feb19", "11 puestos en Feb19", "11 puestos en Mar19", "10 puestos en Mar19",  "11 puestos en Abr19", "9 puestos en Abr19"), 
  value=c(1,1, 1, 2, 1, 2, 1)
  )
 
# From these flows we need to create a node data frame: it lists every entities involved in the flow
nodes <- data.frame(
  name=c(as.character(links$source), 
  as.character(links$target)) %>% unique()
)
 
# With networkD3, connection must be provided using id, not using real name like in the links dataframe.. So we need to reformat it.
links$IDsource <- match(links$source, nodes$name)-1 
links$IDtarget <- match(links$target, nodes$name)-1
 
# Make the Network
p <- sankeyNetwork(Links = links, Nodes = nodes,
              Source = "IDsource", Target = "IDtarget",
              Value = "value", NodeID = "name", 
              sinksRight=FALSE)
p

